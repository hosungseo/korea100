function n(id, name, lane, stage, article, action, output, options = {}) {
  const articles = Array.isArray(article) ? article : [article];
  return {
    id,
    name,
    lane,
    stage,
    type: options.type ?? "task",
    status: options.status ?? "waiting",
    progress: options.progress ?? 0,
    actor: options.actor ?? lane,
    action,
    input_documents: options.input ?? [],
    output_documents: output,
    deadline: options.deadline ?? null,
    blocker: options.blocker ?? null,
    confidence: 0.95,
    legal_basis: articles.map((item) => ({
      law: LAW,
      article: item,
      text: `${LAW} ${item}에 따른 ${name}`,
    })),
  };
}

function s(source, target, label = null) { return { source, target, type: "sequence", label }; }
function m(source, target, label = null) { return { source, target, type: "message", label }; }
function l(source, target, label = null) { return { source, target, type: "loop", label }; }

function numberEdges(items) {
  const counters = { sequence: 0, message: 0, loop: 0 };
  return items.map((item) => {
    counters[item.type] += 1;
    const prefix = item.type === "sequence" ? "E" : item.type === "message" ? "M" : "L";
    return { id: `${prefix}${String(counters[item.type]).padStart(2, "0")}`, ...item };
  });
}

function updateManifest() {
  const manifest = JSON.parse(fs.readFileSync(MANIFEST_PATH, "utf8"));
  for (const spec of candidates) {
    const entry = { priority: spec.priority, slug: spec.slug, name: spec.name, type: spec.type, category: CATEGORY };
    const existing = manifest.find((item) => item.priority === spec.priority || item.slug === spec.slug);
    if (existing && !OVERWRITE) throw new Error(`manifest 중복: ${spec.priority} ${spec.slug}`);
    if (existing && (existing.priority !== spec.priority || existing.slug !== spec.slug)) throw new Error(`manifest 충돌: ${spec.priority} ${spec.slug}`);
    if (existing) Object.assign(existing, entry);
    else manifest.push(entry);
  }
  manifest.sort((a, b) => a.priority - b.priority);
  fs.writeFileSync(MANIFEST_PATH, `${JSON.stringify(manifest, null, 2)}\n`);
}
